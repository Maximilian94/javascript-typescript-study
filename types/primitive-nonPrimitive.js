//  Primitives
//  Can only save one data, the value, and does not change line an object and array, but you can reasing it
//  Example
let car = "car";
console.log(car); //  car
car.toUpperCase();
console.log(car); //  car (No change)
car[0] = "b";
console.log(car); //  car (No change)

//  reasing it
car = car.toUpperCase();
console.log(car); //    CAR

